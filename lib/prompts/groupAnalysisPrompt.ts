import { NewsArticleInput } from "@/types/news";

/**
 * 주제 그룹 분석용 유저 프롬프트.
 * 유사 주제 칼럼을 자동 그룹핑하고 그룹별 분석을 수행.
 */
export function buildGroupAnalysisPrompt(
  articles: NewsArticleInput[]
): string {
  const block = articles
    .map(
      (a, i) =>
        `--- 칼럼 ${i + 1} ---
ID: ${a.id}
출처: ${a.publisher}
제목: ${a.title}
본문:
${a.content}
---`
    )
    .join("\n\n");

  return `아래 한국 언론사 사설/칼럼 텍스트들을 분석하여, 유사 주제끼리 자동 그룹핑하고 각 그룹별 분석을 수행해주세요.

${block}

분석 요구사항:
1. 비슷한 이슈를 다루는 칼럼들을 같은 그룹으로 묶으세요.
2. 단독 주제의 칼럼은 단독 그룹으로 만들어도 됩니다.
3. 각 그룹에 대해 아래 정보를 생성하세요.

매우 중요한 viewpoint 작성 규칙:

viewpoint는 해당 칼럼 필자가 직접 쓴 것처럼, 필자의 주장을 한 문장으로 요약하세요.
마치 칼럼 필자가 한마디로 정리한 핵심 문장이어야 합니다.
반드시 "~다", "~하다" 등 완결된 문장으로 끝내세요.

viewpoint 올바른 예:
- "오일 쇼크를 에너지 전환 가속화의 기회로 삼아야 한다"
- "에너지 전환 대책에서 원전을 빼면 답이 없다"
- "이란과의 성급한 교섭보다 차분한 실리 외교가 필요하다"
- "호르무즈 해협 문제는 다각적 접근으로 차분히 풀어야 한다"
- "재정적자가 2년째 100조인데 재정 건전화를 더 미룰 수 없다"

viewpoint 잘못된 예 (이렇게 쓰지 마세요):
- "에너지 전환 가속화 기회로 활용해야" ← 미완성 문장
- "차분한 실리 외교 우선" ← 미완성 체언 종결
- "원전 포함 균형 잡힌 에너지 믹스 필요" ← 미완성
- "위기를 에너지 전환의 계기로 해석하는 시각이 강조된다" ← 간접 보고체
- "경향신문은 에너지 전환을 주장한다" ← 언론사 주어

commonThemes, coreSummary 작성 규칙:
- commonThemes도 "~다" 완결 문장으로 쓰세요.
- coreSummary는 완결된 문장으로 된 짧은 문단으로 쓰세요.
- 간접 보고체("~라는 의견이 제시된다", "~것으로 보인다") 금지.

추가 규칙:
- 언론사명을 viewpoint 문장에 넣지 마세요. publisher 필드에만 넣으세요.
- viewpoint는 30~50자 내외 한 문장으로 쓰세요.

아래 JSON 형식으로만 응답하세요.

{
  "groups": [
    {
      "groupTitle": "그룹 제목 (예: 에너지·중동 리스크)",
      "articleIds": ["칼럼ID1", "칼럼ID2"],
      "commonThemes": [
        "핵심 요지형 간결 문장 (보고체 금지)",
        "핵심 요지형 간결 문장"
      ],
      "columnPerspectiveDiffs": [
        {
          "publisher": "출처 언론사명",
          "viewpoint": "칼럼 핵심 주장 개조식 한 줄 요약"
        }
      ],
      "coreSummary": "핵심 요약 한 문단 (간접 보고체 금지, 핵심 요지형으로 작성)",
      "soWhatQuestions": [
        "어쩌라고? 질문1",
        "어쩌라고? 질문2",
        "어쩌라고? 질문3"
      ]
    }
  ]
}`;
}
